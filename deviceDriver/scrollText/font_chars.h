// Hàm chuyển ký tự thành index font
int char_to_index(char c)
{
    if (c >= 'A' && c <= 'Z')
        return c - 'A';
    if (c >= 'a' && c <= 'z')
        return c - 'a'; // lowercase = uppercase
    if (c >= '0' && c <= '9')
        return c - '0' + 26;
    if (c == ' ')
        return 36;
    if (c == ',')
        return 37;
    if (c == '.')
        return 38;
    if (c == ':')
        return 39;
    return 36; // default space
}

